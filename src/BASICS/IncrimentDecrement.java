package BASICS;

public class IncrimentDecrement {
    public static void main(String[] args) {
        int x = 5;
        x++;                   // USE X AND THEN INCREMENT(POST INCREMENT)
        System.out.println(x);
        x--;
        System.out.println(x);
        ++x;                    // INCREMENT X AND THEN USE(PRE INCREMENT)
        System.out.println(x);
        --x;
        System.out.println(x);
    }

}
